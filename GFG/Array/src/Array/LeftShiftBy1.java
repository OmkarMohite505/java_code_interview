package Array;

import java.util.Arrays;

public class LeftShiftBy1 {

	public static void main(String[] args) {

		int arr[] = { 1, 2, 5, 7, 9, 8 };
		int first = arr[0];
		int len = arr.length;
		for (int i = 0; i < len - 1; i++) {
			arr[i] = arr[i + 1];
		}

		arr[len - 1] = first;
		System.out.println(Arrays.toString(arr));
	}

}
