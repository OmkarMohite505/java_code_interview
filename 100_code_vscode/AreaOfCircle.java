public class AreaOfCircle {
    
    public static void main(String[] args) {
        double radius = 7;
        double area = 3.14 * radius * radius;
        System.out.println(area);


        // using radius
        double diameter = 14;
        double area2 = (3.14 * diameter * diameter) / 4;
        System.out.println(area2);
    }
}
